void drawBlock(block obj){
	attron(COLOR_PAIR(obj.colour));
	mvaddch(obj.y,obj.x,obj.character);
	attroff(COLOR_PAIR(obj.colour));
}

void drawMap(char data[HEIGHT][WIDTH]){
	int x,y;
	for (y = 0; y < HEIGHT; y++){
		for (x = 0; x < WIDTH; x++) {
			//attron(COLOR_PAIR(data[y][x]));
			mvaddch(y,x,data[y][x]);
			//attron(COLOR_PAIR(data[y][x]));	
		}
	}
}

void drawBullets(){
	int i;
	for (i = 0; i < MAX_BULLETS; i++){
		if (bullets[i].life > -1) {
			char c;
			switch (bullets[i].direction) {
				case north:
				case south:
					c = '|';
					break;
				case east:
				case west:
					c = '-';
					break;
			}
			attron(COLOR_PAIR(RED));
			mvaddch(bullets[i].y,bullets[i].x,c);
			attroff(COLOR_PAIR(RED));
		}
	}
}

void updateBullets(){
	int i;
	for (i = 0; i < MAX_BULLETS; i++) {
		if (bullets[i].life > -1) {
			switch (bullets[i].direction) {
				case north:
					bullets[i].y-=bullets[i].speed;
					break;
				case south:
					bullets[i].y+=bullets[i].speed;
					break;
				case east:
					bullets[i].x+=bullets[i].speed;
					break;
				case west:
					bullets[i].x-=bullets[i].speed;
					break;
			}
			bullets[i].life--;
			if (!canMove(bullets[i].x,bullets[i].y)) { bullets[i].life = -1; }
			if (bullets[i].life == -1) {
				nextBullet = i;
			}
		}
	}
}

void fireBullet(block from, int speed, int damage){
	bullets[nextBullet].x = from.x;
	bullets[nextBullet].y = from.y;
	bullets[nextBullet].direction = from.direction;
	bullets[nextBullet].speed = speed;
	bullets[nextBullet].damage = damage;
	bullets[nextBullet].life = BULLET_LIFE;
	nextBullet++;
}

int canMove(int x, int y) {
	if (map[y][x] == '+') {
		return 1;
	}	else {
		return 0;
	}
}

void render(){
	clear();
	drawMap(map);
	drawBullets();
	drawBlock(player);
	refresh();
}

void update(){
	updateBullets();
}

void main(){

	initscr();
	cbreak();
	nodelay(stdscr, TRUE);
	keypad(stdscr, TRUE);
	noecho();
	curs_set(0);
	start_color();

	init_pair(1,COLOR_WHITE,COLOR_BLACK);
	init_pair(2,COLOR_WHITE,COLOR_BLUE);
	init_pair(RED,COLOR_RED,COLOR_BLACK);
	init_pair(GREEN,COLOR_GREEN,COLOR_BLACK);


	int step = STEP*1000;
	int in;
	int running = 1;
	while (running) {
		usleep(step);
		in = getch();
		switch(in){
			case 'q':
				running = 0;
				break;
			case KEY_UP:
				player.direction = north;
				if (canMove(player.x,player.y-1)) { player.y--; }
				break;
			case KEY_DOWN:
				player.direction = south;
				if (canMove(player.x,player.y+1)) { player.y++; }
				break;
			case KEY_RIGHT:
				player.direction = east;
				if (canMove(player.x+1,player.y)) { player.x++; }
				break;
			case KEY_LEFT:
				player.direction = west;
				if (canMove(player.x-1,player.y)) { player.x--; }
				break;
			case 'z':
				fireBullet(player,1,1);
			default:
				break;
		}
		update();
		render();
	}
	endwin();
	return;
}

